# importando bibliotecas
import pandas as pd
from matplotlib import pyplot as plt

#Criando os dataframe para cada base de dados
df = pd.read_excel("Athletes.xlsx") 
df1 = pd.read_excel("Coaches.xlsx") 
df2 = pd.read_excel("EntriesGender.xlsx") 
df3 = pd.read_excel("Medals.xlsx") 
df4 = pd.read_excel("Teams.xlsx")

#Menu opção para visualizar determinado dado
